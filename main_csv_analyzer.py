import os
import pandas as pd
from dotenv import load_dotenv
from crewai import Agent, Task, Crew, Process, LLM
from crewai.tools import tool

# Загружаем переменные окружения
load_dotenv()
gemini_key = os.getenv("GEMINI_API_KEY")

if not gemini_key or gemini_key == "your_gemini_api_key_here":
    print("\n[ВНИМАНИЕ] Пожалуйста, укажите ваш реальный API-ключ в файле .env!")
    exit(1)

# Настройка языковой модели
my_llm = LLM(
    model="gemini/gemini-2.5-flash",
    api_key=gemini_key
)

# =====================================================================
# ШАГ 1. КАСТОМНЫЙ ИНСТРУМЕНТ ДЛЯ АНАЛИЗА ДАННЫХ (PANDAS + @TOOL)
# Вместо того чтобы скармливать нейросети всю таблицу "в лоб" 
# (что неэффективно для больших файлов), мы пишем инструмент, который 
# делает предобработку с помощью pandas и возвращает готовую агрегированную сводку.
# =====================================================================

@tool("Прочитать и рассчитать статистику по CSV")
def read_and_summarize_csv(file_path: str) -> str:
    """Загружает CSV-файл с данными студентов и рассчитывает ключевые 
    статистические показатели для анализа. 
    Входной параметр file_path — путь к файлу на диске."""
    
    if not os.path.exists(file_path):
        return f"Ошибка: файл {file_path} не найден."
    
    try:
        # Считываем данные с помощью pandas
        df = pd.read_csv(file_path)
        
        # 1. Считаем общую статистику
        total_students = len(df)
        avg_hours = df['Hours_Studied'].mean()
        avg_score = df['Exam_Score'].mean()
        
        # 2. Считаем процент сдавших (Passed == "Да")
        passed_count = len(df[df['Passed'] == 'Да'])
        pass_rate = (passed_count / total_students) * 100
        
        # 3. Анализ по предметам (группировка)
        subject_stats = df.groupby('Subject').agg(
            Average_Hours=('Hours_Studied', 'mean'),
            Average_Score=('Exam_Score', 'mean'),
            Total_Students=('Student_ID', 'count')
        ).to_string()
        
        # 4. Рассчитываем коэффициент корреляции Пирсона 
        # (показывает силу и направление связи между часами учебы и оценками)
        correlation = df['Hours_Studied'].corr(df['Exam_Score'])
        
        # 5. Разница в часах учебы между сдавшими и провалившими
        passed_hours = df[df['Passed'] == 'Да']['Hours_Studied'].mean()
        failed_hours = df[df['Passed'] == 'Нет']['Hours_Studied'].mean()
        
        # Формируем красивый текстовый отчет для агента
        summary = (
            f"=== СТАТИСТИЧЕСКАЯ СВОДКА ПО ФАЙЛУ: {file_path} ===\n\n"
            f"1. Общие показатели:\n"
            f"   - Всего студентов в выборке: {total_students}\n"
            f"   - Среднее время подготовки: {avg_hours:.1f} часов\n"
            f"   - Средний балл за экзамен: {avg_score:.1f}\n"
            f"   - Процент сдачи (успешность): {pass_rate:.1f}%\n\n"
            f"2. Связь факторов (Корреляция):\n"
            f"   - Коэффициент корреляции между часами подготовки и оценкой: {correlation:.4f}\n"
            f"     (Интерпретация: >0.7 — сильная связь, 0.5-0.7 — умеренная, <0.3 — слабая)\n"
            f"   - Среднее время учебы сдавших студентов: {passed_hours:.1f} ч.\n"
            f"   - Среднее время учебы проваливших студентов: {failed_hours:.1f} ч.\n\n"
            f"3. Статистика по дисциплинам:\n"
            f"{subject_stats}\n"
        )
        return summary
        
    except Exception as e:
        return f"Произошла ошибка при анализе данных: {e}"

# =====================================================================
# ШАГ 2. НАСТРОЙКА АГЕНТА И ЗАДАЧИ
# =====================================================================

# Агент-Аналитик
data_analyst = Agent(
    role="Главный аналитик данных",
    goal="Проанализировать статистическую сводку по студентам и написать понятный отчет для руководства",
    backstory=(
        "Вы — опытный Data Scientist и бизнес-аналитик. Вы умеете находить скрытые закономерности в цифрах, "
        "объяснять статистические термины (например, корреляцию) простым языком "
        "и давать четкие, основанные на данных рекомендации."
    ),
    tools=[read_and_summarize_csv],  # Передаем наш кастомный инструмент анализа
    verbose=True,
    llm=my_llm
)

# Задача анализа
task_analyze_csv = Task(
    description=(
        "Изучите файл '{csv_file}' с помощью инструмента 'read_and_summarize_csv'. "
        "Напишите структурированный отчет на РУССКОМ языке. В отчете обязательно раскройте:\n"
        "1. Вводную часть (общие метрики по группе студентов, процент успеваемости).\n"
        "2. Анализ дисциплин (по какому предмету оценки выше, где студенты тратят больше времени).\n"
        "3. Анализ взаимосвязи (подробно объясните корреляцию между временем учебы и итоговым баллом. "
        "Действительно ли часы учебы влияют на результат?).\n"
        "4. Выводы и практические рекомендации (например, какое минимальное количество часов "
        "необходимо готовиться студенту, чтобы сдать экзамен с вероятностью более 90%).\n"
        "Оформите отчет в красивом стиле Markdown с таблицами и списками."
    ),
    expected_output="Подробный аналитический отчет по успеваемости студентов в формате Markdown.",
    agent=data_analyst,
    output_file="data_analysis_report.md"  # Автоматически запишет результат в этот файл
)

# =====================================================================
# ШАГ 3. СБОРКА И ЗАПУСК
# =====================================================================
crew = Crew(
    agents=[data_analyst],
    tasks=[task_analyze_csv],
    process=Process.sequential,
    verbose=True
)

if __name__ == "__main__":
    print("\n=== Запуск агента-аналитика данных ===\n")
    
    # Указываем имя нашего CSV-файла
    target_csv = "student_performance.csv"
    
    result = crew.kickoff(inputs={"csv_file": target_csv})
    
    print("\n=== Анализ завершен! ===")
    print("Результат сохранен в файл: data_analysis_report.md")
    print("Посмотрите его в вашей IDE!\n")
