import os
from dotenv import load_dotenv

# Импортируем класс Agent из библиотеки CrewAI.
from crewai import Agent

# Импортируем необходимые инструменты для агентов и функцию балансировки моделей.
from src.tools import search_web, read_pdf_file, read_text_file, read_docx_file, generate_chart, search_pubmed, search_openalex, search_arxiv, read_powerpoint_file
from src.llm_balancer import get_balanced_llm

def get_agents(topic: str, preset: str = "ranepa", custom_context: str = "", custom_researcher: str = "", custom_critic: str = ""):
    """
    Динамическая инициализация ИИ-агентов только в момент запуска исследования (Lazy Loading).
    Это предотвращает загрузку тяжелых LLM и CrewAI в память при простом импорте модуля.
    """
    # Загружаем ключи из .env
    load_dotenv()

    # Инициализация ИИ-моделей (LLM) для каждого агента индивидуально
    selector_llm = get_balanced_llm("selector")
    researcher_llm = get_balanced_llm("researcher")
    critic_llm = get_balanced_llm("critic")
    designer_llm = get_balanced_llm("designer")

    # Дефолтные значения (РАНХиГС / Горадминистрация)
    selector_role = "Исследователь-Навигатор РАНХиГС"
    selector_goal = "Определить наиболее релевантные научные и веб-источники для проведения исследования по теме '{topic}'"
    selector_backstory = (
        "Вы — главный библиограф, специалист по наукометрии и поисковым системам РАНХиГС. "
        "Вы точно знаете, какие базы данных содержат нужную информацию: PubMed — для медицинских, экологических и биологических тем, "
        "arXiv — для физики, математики, финансов и IT, OpenAlex — для междисциплинарных социально-экономических тем. "
        "Вы формируете точечный план поиска с запросами, чтобы ваши коллеги-исследователи не тратили время на лишние базы."
    )

    researcher_role = "Главный исследователь РАНХиГС"
    researcher_goal = "Провести глубокое научное исследование по теме '{topic}' согласно методическим требованиям"
    researcher_backstory = (
        "Вы — ведущий научный сотрудник РАНХиГС. Вы мастер поиска академической литературы, "
        "сбора муниципальной статистики и структурирования социально-экономических проблем. "
        "Вы ориентированы на практические проекты для муниципаных органов власти и умеете работать "
        "как с сырыми данными пользователя, так и проводить исследование с нуля."
    )

    critic_role = "Представитель администрации города / Рецензент РАНХиГС"
    critic_goal = "Критически оценить результаты исследования по теме '{topic}', указать на слабые места и потребовать доработки"
    critic_backstory = (
        "Вы — вице-мэр города по экономике и развитию и одновременно строгий председатель комиссии РАНХиГС. "
        "Вы не терпите абстрактных рассуждений и 'воды'. Вы требуете чёткого обоснования: "
        "какой социальный эффект получит город? Каковы финансовые затраты и окупаемость для бюджета? "
        "Вы отправляете работу на доработку, пока в ней не будет чётких цифр, логики и пользы для муниципалитета."
    )

    # Меняем в зависимости от выбранного пресета (ролей)
    if preset == "business":
        selector_role = "Бизнес-Навигатор и Маркетолог"
        selector_goal = "Определить ключевые базы данных и источники для анализа рынка по теме '{topic}'"
        selector_backstory = (
            "Вы — специалист по маркетинговым исследованиям и анализу рынков. "
            "Вы знаете, где искать данные о конкурентах, потребительских трендах, объемах рынков и целевой аудитории. "
            "Вы формируете план поиска информации для создания коммерческого обоснования стартапа."
        )
        
        researcher_role = "Главный бизнес-аналитик стартапа"
        researcher_goal = "Разработать жизнеспособную бизнес-модель и провести анализ рынка для темы '{topic}'"
        researcher_backstory = (
            "Вы — опытный продуктовый аналитик и специалист по развитию бизнеса (BizDev). "
            "Вы умеете рассчитывать объемы рынков (TAM/SAM/SOM), анализировать конкурентную среду, "
            "выделять сегменты целевой аудитории и описывать Unit-экономику проекта. "
            "Вы готовите содержательную бизнес-часть для защиты перед инвесторами."
        )
        
        critic_role = "Венчурный инвестор / Бизнес-ангел"
        critic_goal = "Оценить коммерческий потенциал и финансовую окупаемость проекта по теме '{topic}'"
        critic_backstory = (
            "Вы — управляющий партнер венчурного фонда. Вы крайне скептичны. "
            "Вы требуете жесткого обоснования: какова окупаемость инвестиций (ROI), стоимость привлечения клиента (CAC), "
            "его жизненный цикл (LTV) и маржинальность. Вы отправляете проект на доработку, "
            "пока бизнес-модель не станет финансово прозрачной и перспективной."
        )
        
    elif preset == "academic":
        selector_role = "Научный библиограф"
        selector_goal = "Найти фундаментальные научные публикации и методологические базы для темы '{topic}'"
        selector_backstory = (
            "Вы — заведующий библиотекой Академии наук. Вы эксперт по Scopus, Web of Science, Google Scholar и OpenAlex. "
            "Вы отсеиваете ненаучные публикации, формируя список фундаментальных академических трудов и методологических источников."
        )
        
        researcher_role = "Старший научный сотрудник"
        researcher_goal = "Провести строгое академическое исследование по теме '{topic}' с обоснованием научной новизны"
        researcher_backstory = (
            "Вы — ученый с высокой публикационной активностью. Вы эксперт в методологии исследований, "
            "теоретическом анализе и качественном и количественном контент-анализе. Вы формулируете цели, "
            "задачи, выдвигаете научные гипотезы, анализируете аналогичные теории и делаете строгие научные выводы."
        )
        
        critic_role = "Научный рецензент / Доктор наук"
        critic_goal = "Оценить научную новизну, методологию и достоверность выводов исследования по теме '{topic}'"
        critic_backstory = (
            "Вы — строгий рецензент ведущего научного журнала (ВАК / Scopus Q1). "
            "Вы не терпите публицистического стиля, плагиата или необоснованных выводов. "
            "Вы требуете четкого описания методологии, доказательства научной новизны и строгой логики. "
            "Вы возвращаете статью, пока она не будет соответствовать высочайшим стандартам науки."
        )
        
    elif preset == "custom" and custom_context:
        # Инжектируем пользовательский контекст в дефолтных агентов
        researcher_backstory += f"\n[ОСОБОЕ ТРЕБОВАНИЕ ПОЛЬЗОВАТЕЛЯ ДЛЯ ИССЛЕДОВАНИЯ]: {custom_context}"
        critic_backstory += f"\n[ОСОБОЕ ТРЕБОВАНИЕ ПОЛЬЗОВАТЕЛЯ ДЛЯ КРИТИКИ]: {custom_context}"
        critic_goal += f" (с акцентом на требование: '{custom_context}')"

    if custom_researcher:
        researcher_role = "Кастомный исследователь"
        researcher_goal = f"Провести исследование по теме '{topic}' с акцентом на: {custom_researcher}"
        researcher_backstory = (
            f"Вы — специализированный исследователь. Ваша роль и бэкграунд: {custom_researcher}. "
            f"Вы собираете информацию по теме '{topic}', используя научные базы данных и веб-поиск."
        )

    if custom_critic:
        critic_role = "Кастомный критик"
        critic_goal = f"Оценить результаты исследования по теме '{topic}' согласно критериям: {custom_critic}"
        critic_backstory = (
            f"Вы — строгий кастомный эксперт-рецензент. Ваши критерии оценки и требования: {custom_critic}. "
            f"Вы анализируете работу и возвращаете ее на доработку, если требования не выполнены."
        )

    # Агент 0: Навигатор источников (Маршрутизатор)
    tool_selector = Agent(
        role=selector_role,
        goal=selector_goal,
        backstory=selector_backstory,
        verbose=True,
        llm=selector_llm
    )

    # Агент 1: Исследователь
    researcher = Agent(
        role=researcher_role,
        goal=researcher_goal,
        backstory=researcher_backstory,
        tools=[search_web, read_pdf_file, read_text_file, read_docx_file, read_powerpoint_file, search_pubmed, search_openalex, search_arxiv],
        verbose=True,
        llm=researcher_llm
    )

    # Агент 2: Критик
    critic = Agent(
        role=critic_role,
        goal=critic_goal,
        backstory=critic_backstory,
        verbose=True,
        llm=critic_llm
    )

    # Агент 3: Дизайнер слайдов
    designer_backstory = (
        "Вы — профессиональный дизайнер презентаций для топ-менеджеров и госслужащих. "
        "Ваше кредо — краткость и наглядность. Вы умеете сокращать абзацы текста до емких тезисов (буллетов). "
        "Вы анализируете цифры в тексте, находите те, что можно визуализировать, и с помощью инструмента "
        "генерации графиков строите диаграммы для слайдов."
    )
    if preset == "business":
        designer_backstory += " Особое внимание уделяйте визуализации графиков выручки, долей рынка и финансовых показателей."
    elif preset == "academic":
        designer_backstory += " Делайте упор на строгие научные диаграммы, схемы методологии и сравнение показателей."
        
    slide_designer = Agent(
        role="Главный дизайнер презентаций",
        goal="Преобразовать текст исследования по теме '{topic}' в лаконичный формат слайдов и сгенерировать графику",
        backstory=designer_backstory,
        tools=[generate_chart],
        verbose=True,
        llm=designer_llm
    )

    return tool_selector, researcher, critic, slide_designer
