from typing import List, Optional
# Pydantic — библиотека для валидации данных и управления конфигурацией на основе аннотаций типов Python.
from pydantic import BaseModel, Field
from crewai import Task

# =====================================================================
# СХЕМЫ PYDANTIC ДЛЯ ВАЛИДАЦИИ ВЫВОДА (ЗАЩИТА ОТ ГАЛЛЮЦИНАЦИЙ)
# Эти классы описывают точную структуру, которую должен сгенерировать 
# Slide Designer. Любое отклонение от схемы вызовет авто-исправление.
# =====================================================================

class ChartData(BaseModel):
    chart_type: str = Field(description="Тип графика: 'bar' (столбчатый), 'line' (линейный) или 'pie' (круговой)")
    title: str = Field(description="Заголовок графика на русском языке")
    labels: List[str] = Field(description="Список подписей по оси X (например, ['2023', '2024', '2025'])")
    values: List[float] = Field(description="Список числовых значений (например, [15.0, 24.5, 30.0])")
    filename: str = Field(description="Имя сохраняемого файла картинки (например, 'slide_4_chart.png')")

class Slide(BaseModel):
    slide_number: int = Field(description="Порядковый номер слайда от 1 до 10")
    title: str = Field(description="Краткий заголовок слайда на русском языке")
    bullets: List[str] = Field(description="Список тезисов (буллетов) для слайда. Не более 4 штук, каждый тезис - до 7 слов.")
    image: Optional[str] = Field(default=None, description="Имя файла сгенерированного графика (например, 'slide_4_chart.png') или null, если графика нет")
    chart_data: Optional[ChartData] = Field(default=None, description="Данные для автоматической генерации графика в Python, если график нужен на этом слайде (обязательно для Слайда 4 и Слайда 7)")
    image_keywords: Optional[str] = Field(default=None, description="Английские ключевые слова для поиска тематической картинки, если графика нет (например, 'science research lab' или 'active tourism')")

class Presentation(BaseModel):
    slides: List[Slide] = Field(description="Список из 10 слайдов презентации")

class RoadmapRow(BaseModel):
    num: str = Field(description="Номер подпункта (например, '1.1.', '1.2.', '2.1.')")
    task_name: str = Field(description="Наименование задачи или конкретного мероприятия")
    start_date: str = Field(description="Срок начала (например, 'Q1 2026' или 'Июнь 2026')")
    end_date: str = Field(description="Срок окончания (например, 'Q2 2026' или 'Июль 2026')")
    responsible: str = Field(description="Ответственный исполнитель (например, 'Департамент транспорта г. Севастополя', 'IT-подрядчик')")
    result: str = Field(description="Ожидаемый измеримый результат")

class RiskRow(BaseModel):
    num: str = Field(description="Порядковый номер риска (например, '1', '2')")
    risk_name: str = Field(description="Наименование и описание риска")
    mitigation: str = Field(description="Что делаем для минимизации риска (превентивные меры)")
    plan_b: str = Field(description="Что делаем в случае наступления риска (план реагирования)")

class StakeholderRow(BaseModel):
    organization: str = Field(description="Наименование организации или ведомства")
    position: str = Field(description="Должность ключевого лица (например, 'Вице-мэр по финансам')")
    expectations: str = Field(description="Ожидания от проекта или интересы стороны")

class CommRow(BaseModel):
    stakeholder: str = Field(description="Заинтересованная сторона (например, 'Жители Нахимовского района')")
    topic: str = Field(description="Направление взаимодействия / тема коммуникации")
    method: str = Field(description="Способ коммуникации (например, 'Общественные слушания', 'Сайт проекта')")
    frequency: str = Field(description="Периодичность или срок (например, 'Ежемесячно', 'На этапе Q2')")

class ReportData(BaseModel):
    topic: str = Field(description="Тема проекта на русском языке, точно совпадающая с темой исследования")
    
    # Аннотационная таблица (Table 0)
    relevance: str = Field(description="Актуальность темы проекта (краткий абзац до 50 слов)")
    problem: str = Field(description="Ключевая проблема, на решение которой направлен проект (краткий абзац до 50 слов)")
    goal_and_tasks: str = Field(description="Цель и задачи проекта (краткий абзац до 50 слов)")
    audience: str = Field(description="Целевая аудитория проекта (краткий абзац до 50 слов)")
    essence: str = Field(description="Суть решения (кратко, в чем состоит проектное предложение, до 50 слов)")
    effects: str = Field(description="Результаты и эффекты проекта (основные метрики, до 50 слов)")
    
    # Разделы отчета
    section_1_scale: str = Field(description="Очень подробный и развернутый текст для Раздела 1 'Масштаб проектного решения' (отрасль, география, аудитория, нормативная база, не менее 250-350 слов)")
    section_2_problems: str = Field(description="Очень подробный и развернутый текст для Раздела 2 'Обоснование ключевых проблем' (измеримая статистика, динамика показателей, корневые причины, не менее 300-450 слов)")
    section_3_goal_tasks: str = Field(description="Очень подробный и развернутый текст для Раздела 3 'Цель и задачи проекта' (цели по SMART, промежуточные индикаторы, описание задач, не менее 300-450 слов)")
    section_4_novelty: str = Field(description="Очень подробный и развернутый текст для Раздела 4 'Обоснование новизны проектного решения' (уникальность, отличие от существующих практик, почему сработает, результаты опросов, не менее 300-450 слов)")
    
    # Дорожная карта (Table 1)
    roadmap: List[RoadmapRow] = Field(description="Календарный план из 6-9 мероприятий дорожной карты проекта")
    
    section_6_results: str = Field(description="Очень подробный и развернутый текст для Раздела 6 'Результаты и эффекты проекта' (количественные и качественные социальные и финансовые эффекты, не менее 300-450 слов)")
    
    # Риски (Table 2)
    risks: List[RiskRow] = Field(description="Анализ рисков проекта из 3-4 строк таблицы рисков")
    
    # Списки литературы
    literature: List[str] = Field(description="Список литературы и нормативных правовых актов, использованных при исследовании (5-8 источников)")
    
    # Заинтересованные стороны (Table 3)
    stakeholders: List[StakeholderRow] = Field(description="Перечень ключевых заинтересованных сторон из 4-5 строк")
    
    # План коммуникаций (Table 4)
    communication_plan: List[CommRow] = Field(description="План коммуникаций с заинтересованными сторонами из 4-5 строк")
    
    # Приложения к отчету (1-4)
    appendix_1_problems: str = Field(description="Развернутый анализ проблемного поля проекта для Приложения 1 (корневые причины, взаимосвязи проблем, дерево проблем, от 300 до 500 слов)")
    appendix_2_solutions: str = Field(description="Подробная карта идей-решений проекта для Приложения 2 (структурированное описание предлагаемых мер, концепций, решений и их логики, от 300 до 500 слов)")
    appendix_3_practices: str = Field(description="Подробный обзор отечественных и зарубежных практик решения для Приложения 3 (анализ аналогичных проектов в РФ и мире, их эффекты, применимость, от 300 до 500 слов)")
    appendix_4_target_groups: str = Field(description="Подробное описание инструментов работы с целевыми группами для Приложения 4 (методы опросов, фокус-групп, конкретные анкетные вопросы, результаты интервью, от 300 до 500 слов)")

def get_tasks(topic: str, uploaded_file_path: str, user_images_prompt: str, research_text: str, agents: tuple):
    """
    Динамическая инициализация задач для ИИ-агентов (Lazy Loading).
    Создает и форматирует задачи только в момент непосредственного запуска CrewAI.
    """
    tool_selector, researcher, critic, slide_designer = agents

    task_tool_selection = Task(
        description=(
            "Проанализируйте тему исследования: '{topic}'.\n"
            "Определите, какие специализированные базы данных и типы поисковых запросов необходимы для сбора качественного научного материала:\n"
            "- arXiv: подходит для точных наук, IT, ИИ, математики, финансов.\n"
            "- PubMed: подходит для медицины, здоровья населения, экологии, биологии, химии.\n"
            "- OpenAlex: междисциплинарный поиск (социология, экономика, урбанистика, менеджмент, общие темы).\n"
            "- Веб-поиск (search_web): для региональной специфики городов РФ, локальных новостей, постановлений властей.\n"
            "Сформируйте четкий пошаговый план поиска. Напишите точные поисковые запросы на английском языке "
            "для специализированных научных баз и на русском — для веб-поиска."
        ).format(topic=topic),
        expected_output="План научного поиска с указанием баз данных и точных запросов.",
        agent=tool_selector
    )

    task_research = Task(
        description=(
            "Проведите исследование по теме: '{topic}'.\n"
            "Ознакомьтесь с планом научного поиска, составленным Навигатором источников на предыдущем шаге.\n"
            "Используйте указанные им базы данных (через инструменты 'search_pubmed', 'search_openalex', "
            "'search_arxiv' или 'search_web') и отправьте соответствующие запросы для сбора уникальной информации, "
            "статистики и реальных научных фактов.\n"
            "Если пользователь передал путь к файлу с готовой работой в параметре '{uploaded_file_path}' (и файл существует), "
            "вы обязаны прочитать этот файл (используя инструмент 'Прочитать текстовый файл', 'Чтение PDF-файлов', 'read_docx_file' или 'read_powerpoint_file' в зависимости от расширения файла) и взять его за основу.\n"
            "Если в папке проекта находится файл методички по исследованию в формате PDF (например, 'manual.pdf'), вы обязательно должны прочитать его с помощью инструмента 'Чтение PDF-файлов', чтобы понять требования к структуре и целям.\n"
            "Ваша цель - составить подробный текст исследования, строго придерживаясь методических требований из файла 'config/guidelines.md':\n"
            "- Сформулируйте введение, актуальность, объект, предмет и цели.\n"
            "- Опишите проблему в городском хозяйстве.\n"
            "- Предложите конкретный проект (мероприятия) для администрации города.\n"
            "- Приведите детальные расчеты: социальный эффект (рабочие места, экология, благополучатели) "
            "и финансовое обоснование (затраты бюджета, окупаемость, экономия).\n"
            "- ОБЯЗАТЕЛЬНО детально и развернуто распишите четыре приложения: "
            "1) Развернутый анализ проблемного поля и корневых причин; "
            "2) Карта идей-решений (структурированный список мер); "
            "3) Обзор отечественных и зарубежных практик решения (не менее 2-3 кейсов); "
            "4) Инструменты работы с целевыми группами (опросы, анкетные вопросы, методы фокус-групп)."
        ).format(topic=topic, uploaded_file_path=uploaded_file_path),
        expected_output="Подробный текст исследования по разделам методички с конкретными цифрами, фактами и ссылками на научные статьи.",
        agent=researcher
    )

    task_critique = Task(
        description=(
            "Изучите текст исследования по теме '{topic}', написанный Researcher на предыдущем шаге.\n"
            "Проведите аудит как строгий рецензент РАНХиГС и вице-мэр города:\n"
            "- Достаточно ли в работе конкретных цифр и расчетов?\n"
            "- Показана ли явная польза для города (социальная или финансовая)?\n"
            "- Подробно ли расписаны разделы и приложения (1-4)?\n"
            "- Есть ли логические нестыковки?\n"
            "Напишите список конкретных, жестких замечаний и укажите, какие именно разделы и данные "
            "Researcher должен переделать или добавить."
        ).format(topic=topic),
        expected_output="Рецензионное заключение со списком замечаний и требований по доработке.",
        agent=critic
    )

    task_revision = Task(
        description=(
            "Возьмите ваш первоначальный текст исследования и список замечаний от Критика.\n"
            "Полностью переработайте исследование по теме '{topic}', исправив ВСЕ недочеты.\n"
            "Добавьте конкретные цифры бюджета, показатели окупаемости, социальные метрики и логику.\n"
            "Убедитесь, что все основные разделы и приложения (1-4) прописаны максимально подробно, академично и развернуто (не менее 500-600 слов на каждое приложение).\n"
            "Итоговый текст должен быть оформлен в красивом стиле Markdown."
        ).format(topic=topic),
        expected_output="Итоговый доработанный текст исследования в формате Markdown.",
        agent=researcher,
        output_file="output/research_text.md"
    )

    task_slide_design = Task(
        description=(
            "На основе следующего итогового исследования:\n"
            "```markdown\n"
            "{research_text}\n"
            "```\n\n"
            "разработайте структуру презентации из 10 слайдов по теме '{topic}', строго следуя правилам оформления из 'config/slide_rules.md'.\n"
            "Для каждого слайда (от 1 до 10) подготовьте заголовок и лаконичные тезисы (буллеты).\n"
            "Выделите в тексте числовые показатели для визуализации.\n"
            "- Выберите подходящий тип графика (bar, line или pie), сформулируйте заголовок на русском, добавьте подписи и соответствующие числовые значения на основе исследования.\n"
            "- ВСЕ подписи, показатели, категории и заголовок графика должны быть строго на русском языке. Категорически запрещено использовать английские сокращения (такие как CAPEX, OPEX, IT, KPI) — переводите их на русский язык (например, Капвложения, Оперрасходы, ИТ, КПЭ).\n"
            "- Имя файла (поле 'image' и 'filename') укажите как 'slide_4_chart.png' и 'slide_7_chart.png' соответственно.\n\n"
            "{user_images_prompt}\n\n"
            "Если для слайда нет подходящего загруженного изображения пользователя, заполните поле 'image_keywords' (на английском языке, например, 'science research lab' или 'active tourism'), чтобы система автоматически подобрала картинку из открытых библиотек.\n\n"
            "Результат должен быть возвращен строго в структурированном виде согласно Pydantic-модели Presentation."
        ).format(topic=topic, research_text=research_text, user_images_prompt=user_images_prompt),
        expected_output="Презентация, разбитая по слайдам, со ссылками на сгенерированные диаграммы в формате Pydantic.",
        agent=slide_designer,
        output_pydantic=Presentation,
        output_file="output/slides_data.json"
    )

    task_report_design = Task(
        description=(
            "На основе следующего итогового исследования:\n"
            "```markdown\n"
            "{research_text}\n"
            "```\n\n"
            "структурируйте информацию для официального Word-отчета по теме '{topic}' согласно Pydantic-модели ReportData.\n"
            "Выделите из текста исследования и подробно распишите:\n"
            "- Краткие абзацы для аннотационной таблицы (актуальность, проблема, цели/задачи, аудитория, суть решения, эффекты).\n"
            "- Очень подробные, объемные и развернутые параграфы для соответствующих разделов (Масштаб, Обоснование проблем, Цель и задачи, Новизна, Результаты/эффекты). Каждое текстовое поле (section_1_scale, section_2_problems, section_3_goal_tasks, section_4_novelty, section_6_results) ОБЯЗАНО содержать развернутый профессиональный текст объемом не менее 500-600 слов (или не менее 3000-4000 символов)!\n"
            "- Детальные, академически полные и объемные тексты для Приложений 1, 2, 3 и 4 на основе исследования (каждое приложение: appendix_1_problems, appendix_2_solutions, appendix_3_practices, appendix_4_target_groups ОБЯЗАНО содержать не менее 500-600 слов)!\n"
            "- Мероприятия дорожной карты с указанием шагов, сроков, ответственных и измеримых результатов.\n"
            "- Анализ рисков с антирисковыми превентивными и реактивными мерами.\n"
            "- Список использованных источников и нормативных документов.\n"
            "- Список заинтересованных сторон и план коммуникаций с ними.\n\n"
            "Результат должен быть возвращен строго в структурированном виде согласно Pydantic-модели ReportData."
        ).format(topic=topic, research_text=research_text),
        expected_output="Структурированный отчет в формате Pydantic для последующей вставки в Word.",
        agent=slide_designer,
        output_pydantic=ReportData,
        output_file="output/report_data.json"
    )

    return task_tool_selection, task_research, task_critique, task_revision, task_slide_design, task_report_design
