import os
from dotenv import load_dotenv
from crewai import Agent, Task, Crew, Process, LLM

# Загружаем переменные окружения
load_dotenv()
gemini_key = os.getenv("GEMINI_API_KEY")

if not gemini_key or gemini_key == "your_gemini_api_key_here":
    print("\n[ВНИМАНИЕ] Пожалуйста, укажите ваш реальный API-ключ в файле .env!")
    exit(1)

# Создаем объект нашей языковой модели
my_llm = LLM(
    model="gemini/gemini-2.5-flash",
    api_key=gemini_key
)

# =====================================================================
# ШАГ 1. СОЗДАНИЕ АГЕНТОВ (3 агента)
# =====================================================================

# 1. Исследователь
researcher = Agent(
    role="Исследователь учебных материалов",
    goal="Собрать подробную, достоверную и структурированную информацию по теме '{topic}'",
    backstory=(
        "Вы — опытный академический исследователь. Вы мастерски находите ключевые концепции, "
        "структурируете сложные научные темы и выделяете суть из большого объема информации."
    ),
    verbose=True,
    llm=my_llm
)

# 2. Писатель/Методист
writer = Agent(
    role="Академический писатель и методист",
    goal="Создать подробный структурированный план работы и описание глав по теме '{topic}'",
    backstory=(
        "Вы — профессор университета и автор учебных пособий. Вы умеете объяснять "
        "сложные вещи простым языком и выстраивать идеальную логику структуры дипломных работ."
    ),
    verbose=True,
    llm=my_llm
)

# 3. Критик / Рецензент (Новый Агент)
critic = Agent(
    role="Строгий академический рецензент",
    goal="Провести критический анализ плана работы по теме '{topic}', найти пробелы и предложить улучшения",
    backstory=(
        "Вы — авторитетный рецензент научных журналов и член диссертационного совета. "
        "Вы ищете логические нестыковки, пропущенные важные темы, неактуальные подходы "
        "и даете конструктивные, строгие указания по улучшению структуры работы."
    ),
    verbose=True,
    llm=my_llm
)

# =====================================================================
# ШАГ 2. ОПРЕДЕЛЕНИЕ ЗАДАЧ (Цепочка из 4 задач)
# =====================================================================

# Задача 1: Первичное исследование (Исследователь)
task_research = Task(
    description=(
        "Проведите исследование по теме: '{topic}'. "
        "Выделите основные направления, ключевые термины и современные тренды."
    ),
    expected_output="Аналитический конспект с терминами и ключевыми аспектами.",
    agent=researcher
)

# Задача 2: Написание чернового плана (Писатель)
task_write_draft = Task(
    description=(
        "Используя исследование от Researcher, составьте первоначальный (черновой) вариант структуры "
        "работы по теме: '{topic}'. Напишите введение, 2-3 основные главы и заключение."
    ),
    expected_output="Черновой структурированный план дипломной работы/презентации.",
    agent=writer
)

# Задача 3: Рецензирование и критика (Критик оценивает черновик Писателя)
task_critique = Task(
    description=(
        "Изучите черновой план, созданный в задаче Task 2. "
        "Критически оцените структуру: логична ли последовательность глав? Все ли важные аспекты "
        "темы '{topic}' учтены? Достаточно ли внимания уделено современным методам? "
        "Напишите подробный список замечаний, правок и упущенных вещей, которые Писатель (Writer) должен исправить."
    ),
    expected_output="Конструктивный рецензионный отчет с перечнем замечаний и правок.",
    agent=critic
)

# Задача 4: Доработка структуры по замечаниям (Писатель исправляет работу)
task_final_revision = Task(
    description=(
        "Изучите первоначальный план из Task 2 и рецензию от Критика из Task 3. "
        "Переработайте и улучшите план работы по теме '{topic}', исправив все указанные критиком недочеты. "
        "Сделайте итоговую структуру глубокой, детальной и академически строгой."
    ),
    expected_output="Готовый высококачественный структурированный отчет в формате Markdown.",
    agent=writer,
    output_file="critic_approved_report.md"  # Запишется в новый файл
)

# =====================================================================
# ШАГ 3. СБОРКА КОМАНДЫ И ЗАПУСК
# =====================================================================
crew = Crew(
    agents=[researcher, writer, critic],
    tasks=[task_research, task_write_draft, task_critique, task_final_revision],
    process=Process.sequential,  # Задачи выполняются последовательно
    verbose=True
)

if __name__ == "__main__":
    print("\n=== Запуск мультиагентной системы с Критиком ===\n")
    user_topic = "Применение машинного обучения в анализе финансовых временных рядов"
    
    result = crew.kickoff(inputs={"topic": user_topic})
    
    print("\n=== Работа завершена! ===")
    print("Итоговый отчет сохранен в файл: critic_approved_report.md")
    print("Посмотрите его в вашей IDE!\n")
