import os
import numpy as np
from dotenv import load_dotenv
from google import genai

# Загружаем ключи
load_dotenv()
api_key = os.getenv("GEMINI_API_KEY")

if not api_key or api_key == "your_gemini_api_key_here":
    print("\n[ВНИМАНИЕ] Пожалуйста, укажите ваш реальный API-ключ в файле .env!")
    exit(1)

# Инициализируем клиент Google GenAI
client = genai.Client(api_key=api_key)

# =====================================================================
# ДАННЫЕ: НАША БАЗА ЗНАНИЙ (Учебник по Data Science)
# Представьте, что это огромный учебник на 1000 страниц.
# Мы разбили его на абзацы (чанки).
# =====================================================================
documents = [
    "1. Линейная регрессия — это метод прогнозирования непрерывной величины на основе линейной зависимости между факторами. Используется для прогнозирования цен на недвижимость, объемов продаж и других числовых показателей.",
    "2. Логистическая регрессия — это алгоритм классификации, который предсказывает вероятность принадлежности объекта к одному из двух классов (например, уйдет клиент или нет, болен пациент или здоров). Выходом является вероятность от 0 до 1.",
    "3. Дерево решений — это структура, похожая на блок-схему, где каждый внутренний узел проверяет условие по признаку, ветви представляют результаты теста, а листья содержат итоговые предсказания классов.",
    "4. Метод случайного леса (Random Forest) — это ансамблевый алгоритм машинного обучения, основанный на объединении множества отдельных деревьев решений для снижения переобучения и повышения точности.",
    "5. Градиентный бустинг (Gradient Boosting) — это метод построения ансамбля, где новые деревья обучаются последовательно, исправляя ошибки предыдущих моделей. Очень эффективен для сложных табличных данных."
]

# =====================================================================
# ШАГ 1. ПОЛУЧЕНИЕ ЭМБЕДДИНГОВ (МАТЕМАТИЧЕСКИХ ВЕКТОРОВ)
# Эмбеддинг — это представление текста в виде вектора из сотен чисел.
# Тексты, похожие по смыслу, будут иметь близкие векторы в многомерном пространстве.
# =====================================================================
def get_embedding(text: str) -> np.ndarray:
    """Делает запрос к модели Google Gemini Embedding и возвращает список чисел (вектор)"""
    response = client.models.embed_content(
        model="gemini-embedding-2",
        contents=text
    )
    # Возвращаем вектор в формате numpy array для удобства математических расчетов
    return np.array(response.embeddings[0].values)

# =====================================================================
# ШАГ 2. ВЫЧИСЛЕНИЕ КОСИНУСНОГО СХОДСТВА (COSINE SIMILARITY)
# Это стандартная метрика в Data Science для определения близости двух векторов.
# Значение лежит от -1 до 1. Чем ближе к 1, тем ближе тексты по смыслу.
# =====================================================================
def cosine_similarity(v1: np.ndarray, v2: np.ndarray) -> float:
    """Вычисляет косинусное сходство между двумя векторами"""
    dot_product = np.dot(v1, v2)
    norm_v1 = np.linalg.norm(v1)
    norm_v2 = np.linalg.norm(v2)
    return dot_product / (norm_v1 * norm_v2)

# =====================================================================
# ШАГ 3. ОСНОВНОЙ ПРОЦЕСС RAG
# =====================================================================
if __name__ == "__main__":
    print("=== ПОДГОТОВКА RAG БАЗЫ ЗНАНИЙ ===")
    print("Генерируем эмбеддинги для абзацев учебника...")
    
    # Получаем векторы для всех наших документов
    doc_embeddings = [get_embedding(doc) for doc in documents]
    print(f"Успешно сгенерировано {len(doc_embeddings)} векторов.")
    print(f"Размерность каждого вектора: {len(doc_embeddings[0])} чисел.\n")
    
    # Вопрос пользователя
    user_query = "Какая модель предсказывает вероятность класса от 0 до 1?"
    print(f"Вопрос пользователя: '{user_query}'\n")
    
    # Получаем вектор вопроса
    query_embedding = get_embedding(user_query)
    
    # Вычисляем сходство вопроса со всеми абзацами
    similarities = []
    for i, doc_emb in enumerate(doc_embeddings):
        score = cosine_similarity(query_embedding, doc_emb)
        similarities.append((score, documents[i]))
        print(f"Абзац {i+1} | Оценка соответствия смыслу (similarity): {score:.4f}")
        
    # Сортируем по убыванию соответствия
    similarities.sort(key=lambda x: x[0], reverse=True)
    
    # Извлекаем самый релевантный абзац
    best_score, best_doc = similarities[0]
    print(f"\n[РЕЗУЛЬТАТ ПОИСКА] Наиболее подходящий абзац (score: {best_score:.4f}):")
    print(f"-> {best_doc}\n")
    
    # =====================================================================
    # ШАГ 4. ОТПРАВКА В LLM (С АВТОМАТИЧЕСКИМ КОНТЕКСТОМ)
    # Мы отправляем модели ТОЛЬКО этот найденный абзац вместо всего учебника!
    # =====================================================================
    print("Отправляем вопрос и найденный абзац в Gemini-2.5-Flash...")
    
    prompt = (
        f"Используй только следующий контекст для ответа на вопрос.\n"
        f"Контекст: {best_doc}\n\n"
        f"Вопрос: {user_query}\n\n"
        f"Ответ:"
    )
    
    response = client.models.generate_content(
        model="gemini-2.5-flash",
        contents=prompt
    )
    
    print("\n=== ОТВЕТ МОДЕЛИ ===")
    print(response.text.strip())
