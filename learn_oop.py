# 1. ОПРЕДЕЛЕНИЕ КЛАССА
# Класс — это "чертеж" или шаблон. Например, чертеж Учебного Ассистента.
# Сам по себе класс ничего не делает, он просто описывает, какими свойствами
# и поведением будут обладать будущие объекты.
class StudyAssistant:
    
    # 2. МЕТОД ИНИЦИАЛИЗАЦИИ (КОНСТРУКТОР)
    # Метод __init__ запускается автоматически, когда мы создаем новый объект (экземпляр).
    # self — это важнейшая штука. Она означает "этот конкретный объект".
    # Через self мы привязываем переменные и функции к конкретному экземпляру.
    def __init__(self, name: str, subject: str, skill_level: int):
        self.name = name                 # Атрибут (свойство) объекта: имя
        self.subject = subject           # Атрибут: учебная дисциплина
        self.skill_level = skill_level   # Атрибут: уровень навыка от 1 до 5
        
    # 3. МЕТОДЫ КЛАССА (ДЕЙСТВИЯ)
    # Метод — это функция, которая живет внутри класса.
    # Первым аргументом метода ВСЕГДА должен быть self, чтобы метод знал,
    # чьи именно свойства (какого объекта) ему использовать.
    def introduce(self):
        print(f"Привет! Я ассистент по имени {self.name}.")
        print(f"Моя специализация: {self.subject}. Мой уровень навыка: {self.skill_level}/5.")

    def write_summary(self, topic: str):
        # Метод использует свойства объекта (self.name, self.skill_level)
        # и обычный параметр, переданный при вызове (topic)
        print(f"\n-> [{self.name}] начинает писать конспект по теме '{topic}'...")
        
        if self.skill_level >= 4:
            return f"Отличный глубокий конспект по теме '{topic}' в рамках '{self.subject}'."
        else:
            return f"Базовый конспект по теме '{topic}' (рекомендуется проверить детали)."

# =====================================================================
# ЗАПУСК И ИСПОЛЬЗОВАНИЕ КЛАССА
# =====================================================================
if __name__ == "__main__":
    print("=== ДЕМОНСТРАЦИЯ ООП В PYTHON ===\n")
    
    # 4. СОЗДАНИЕ ОБЪЕКТОВ (ЭКЗЕМПЛЯРОВ КЛАССА)
    # Здесь мы по чертежу StudyAssistant создаем двух совершенно разных помощников.
    # Мы передаем аргументы, и Python автоматически передает их в __init__ 
    # (self подставляется сам).
    
    assistant_alex = StudyAssistant(name="Алекс", subject="Высшая математика", skill_level=5)
    assistant_daria = StudyAssistant(name="Дарья", subject="История экономики", skill_level=3)
    
    # 5. ВЫЗОВ МЕТОДОВ (ВЫПОЛНЕНИЕ ДЕЙСТВИЙ)
    # Заставляем каждого ассистента представиться.
    # Когда мы пишем assistant_alex.introduce(), внутрь метода introduce()
    # в качестве self передается именно объект assistant_alex.
    assistant_alex.introduce()
    print("-" * 30)
    assistant_daria.introduce()
    print("-" * 30)
    
    # Проверим метод создания конспектов. У Алекса высокий навык (5), у Дарьи — средний (3)
    report_math = assistant_alex.write_summary("Матрицы и векторы")
    print("Результат Алекса:", report_math)
    
    report_history = assistant_daria.write_summary("Промышленный переворот в Англии")
    print("Результат Дарьи:", report_history)
